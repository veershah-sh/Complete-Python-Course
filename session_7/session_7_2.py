# 1. Arithmatic Operators

# + -> addition
res = 10 + 20
print(f"add: {res}")

# - -> subtraction
res_1 = 30 - 20.20
print(f"sub: {res_1}")

# * -> multiplication
res_2 = 40 * 3
print(f"mul: {res_2}")

# / -> division (quotient) (float)
res_3 = 10/2
print(f"div: {res_3}")

# // -> floor division (quotient) (int)
res_4 = 20//3
print(f"f div: {res_4}")

# % -> modulus (reminder) (int)
res_5 = 30 % 2
print(f"mod: {res_5}")

# ** -> exponential  
res_6 = 2 ** 8
print(f"exp: {res_6}")

