# 3. Comparison Operators (Relational)
# this operatos always returns boolean value

num1 = "abc"
num2 = "def"

# == -> is equal to
print(num1 == num2)

# != -> not equal to
print(num1 != num2)

# > -> greater than
print(num1 > num2)

# >= -> greater than OR equal to
print(num1 >= num2)

# < -> less than
print(num1 < num2)

# <= -> less than OR equal to
print(num1 <= num2)